    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String nombre = jTextFieldNombre.getText().trim();
        char[] pass = jPasswordField1.getPassword();
        String passwd = new String (pass);
        if(nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Introduce el nombre");
        } 
        else if(pass.equals("")){
            JOptionPane.showMessageDialog(null, "La contraseña está vacía");
        }
        else{
            ResultSet rs1 =miConexion.comprobarDatos("SELECT * FROM admin WHERE USER = '"+nombre+"' AND pass ='"+passwd+"'");
            System.out.println(rs1);
            if(rs1==null){
                JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos");
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuario correcto");
                menuPrincipal mp = new menuPrincipal();
                mp.setVisible(true);
                this.setVisible(false);
                
            }
            
            
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

